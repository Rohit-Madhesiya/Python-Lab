#operator
a = 20
b = 14.5
#Arithmetic Operators
print("Addition:", a + b)
print("Subtraction:", a - b)
print("Multiplication:", a * b)
print("Power:", a**5)
print("Division:", a / b)
print("Division Floor:", a // b)
#Comparison Operator
x = 10
y = 20
z = 10
print("Comparison Operator:")
print(x <= y)
print(x == z)
print(x >= y)
#Assignment Operator
print("Assignment Operator:")
x += 10
print(x)
y -= 10
print(y)
z **= 2
print(z)
x //= 3
print(x)
#Logical Operator
print("Logical Operator:")
print(a and b)
print(a or b)
#Bitwise Operator
print("Bitwise Operator:")
print(x ^ y)
print(x | y)
print(x & y)
#Membership Operator
print("Membership Operator:")
ls = [10, 20, 30]
print(x in ls)
print(y not in ls)
#Identity Operator
print("Identity Operator:")
print(x is y)
print(y is not z)
