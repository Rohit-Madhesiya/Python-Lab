# Create a tuple
my_tuple = ()
print(my_tuple)
# Add items to the tuple
my_tuple = my_tuple + (1, 2, 3)
print(my_tuple)
# Get the length of the tuple
tuple_length = len(my_tuple)
print(tuple_length)
# Check if an item is in the tuple
item_check = 2 in my_tuple
print(item_check)
# Access items in the tuple
item1 = my_tuple[0]
item2 = my_tuple[1]
print(item1)
print(item2)
